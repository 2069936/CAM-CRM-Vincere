create or replace function public.prevent_ingest_batch_storage_path_change()
returns trigger
language plpgsql
set search_path = pg_catalog, public
as $function$
begin
  if new.storage_path is distinct from old.storage_path then
    raise exception 'ingest batch storage_path is immutable'
      using errcode = '22023';
  end if;
  return new;
end;
$function$;

revoke all on function public.prevent_ingest_batch_storage_path_change()
  from public, anon, authenticated;
grant execute on function public.prevent_ingest_batch_storage_path_change()
  to service_role;

do $trigger$
begin
  if not exists (
    select 1
    from pg_catalog.pg_trigger
    where tgrelid = 'public.ingest_batches'::regclass
      and tgname = 'ingest_batches_storage_path_immutable'
      and not tgisinternal
  ) then
    create trigger ingest_batches_storage_path_immutable
      before update of storage_path on public.ingest_batches
      for each row
      execute function public.prevent_ingest_batch_storage_path_change();
  end if;
end
$trigger$;

-- Private raw-object bucket. The restrictive policy below is ANDed with any
-- permissive policy applicable to anon/authenticated, so even a broad existing
-- Storage policy cannot authorize this bucket. It remains true for other buckets
-- and does not apply to service_role, which retains its normal RLS bypass.
insert into storage.buckets (id, name, public)
values ('ninjatrader-imports', 'ninjatrader-imports', false)
on conflict (id) do update
set name = excluded.name,
    public = false;

do $storage_policy$
begin
  if not exists (
    select 1
    from pg_catalog.pg_policies
    where schemaname = 'storage'
      and tablename = 'objects'
      and policyname = 'ninjatrader-imports deny browser direct access'
  ) then
    create policy "ninjatrader-imports deny browser direct access"
      on storage.objects
      as restrictive
      for all
      to anon, authenticated
      using (bucket_id <> 'ninjatrader-imports')
      with check (bucket_id <> 'ninjatrader-imports');
  end if;
end
$storage_policy$;

