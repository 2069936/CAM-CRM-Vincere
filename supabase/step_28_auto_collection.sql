-- configured interval avoid a write only when every supplied heartbeat field is
-- unchanged. First contact, version/error/status changes, and recovery always
-- bypass throttling. Free-form diagnostic text stays in allowlisted metadata
-- and is deliberately excluded from audit payloads and the RPC result.
create or replace function public.record_ingest_heartbeat(
  p_device_id uuid,
  p_agent_version text,
  p_addon_version text,
  p_ninjatrader_version text,
  p_last_capture_at timestamptz,
  p_last_success_at timestamptz,
