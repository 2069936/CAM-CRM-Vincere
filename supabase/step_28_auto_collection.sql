      and device.revoked_at is null
  ) then
    raise exception 'MACHINE_CONFLICT'
      using errcode = 'P0001';
  end if;
  if exists (
    select 1
    from public.ingest_devices as device
    where device.client_id = v_client_id
      and device.status = 'active'
      and device.revoked_at is null
  ) then
    raise exception 'CODE_CONSUMED'
      using errcode = 'P0001';
