-- Atomically consume a one-time enrollment. The enrollment row lock serializes
-- concurrent calls. A retry with the winning machine and credential hashes gets
-- the same device; different hashes cannot reuse the consumed code.
create or replace function public.pair_ingest_device(
  p_code_hash text,
  p_machine_hash text,
  p_credential_hash text,
  p_credential_prefix text,
  p_agent_version text,
  p_addon_version text
)
returns public.ingest_devices
language plpgsql
security definer
set search_path = pg_catalog, public
as $function$
declare
  v_enrollment public.ingest_enrollments;
  v_device public.ingest_devices;
begin
  if nullif(btrim(p_code_hash), '') is null
    or nullif(btrim(p_machine_hash), '') is null
    or nullif(btrim(p_credential_hash), '') is null
    or nullif(btrim(p_credential_prefix), '') is null then
    raise exception 'pairing digests and credential prefix are required'
      using errcode = '22023';
  end if;

  select enrollment.*
  into v_enrollment
  from public.ingest_enrollments as enrollment
  where enrollment.code_hash = p_code_hash
  for update;

  if not found then
    raise exception 'enrollment code is invalid'
      using errcode = 'P0002';
  end if;

  if v_enrollment.revoked_at is not null then
    raise exception 'enrollment code is revoked'
      using errcode = '22023';
  end if;

  if v_enrollment.consumed_by_device_id is not null then
    select device.*
    into v_device
    from public.ingest_devices as device
    where device.id = v_enrollment.consumed_by_device_id
    for update;

    if not found
      or v_device.machine_id_hash is distinct from p_machine_hash
      or v_device.credential_hash is distinct from p_credential_hash then
