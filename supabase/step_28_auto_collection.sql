-- Step 28: Server-only auto-collection enrollment, device credentials, and batches.
-- Run after step_22_ingest_devices.sql.
--
-- This migration is additive and transaction-wrapped so an error does not leave a
-- partially upgraded schema. CREATE OR REPLACE FUNCTION intentionally replaces
-- the two RPC bodies on rerun while preserving their identities. PostgreSQL also
-- preserves existing function grants on replacement, so the explicit REVOKE and
-- service_role-only GRANT statements below must remain immediately after the RPCs.
-- A future signature or return-type change requires a separately reviewed DROP.
--
-- Hashes and HMACs are generated by the Node API. SQL stores only supplied digest
-- text; enrollment codes and device credentials are never stored in raw form.

