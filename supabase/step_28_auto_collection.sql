    p_machine_hash,
    p_credential_hash,
    p_credential_prefix,
    'active',
    p_agent_version,
    p_addon_version,
    clock_timestamp()
  )
  returning * into v_device;

  update public.ingest_enrollments
  set consumed_at = clock_timestamp(),
      consumed_by_device_id = v_device.id
  where id = v_enrollment.id;

  return v_device;
end;
