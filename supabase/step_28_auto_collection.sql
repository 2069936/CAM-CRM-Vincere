      coalesce(nullif(v_item ->> 'accountBalance', '')::numeric, 0),
      coalesce(nullif(v_item ->> 'weeklyPnl', '')::numeric, 0),
      coalesce(nullif(v_item ->> 'unrealizedPnl', '')::numeric, 0)
