-- CHECK constraints cannot contain a subquery. This immutable helper validates
-- every dynamic row-count value while keeping the batch constraint declarative.
create or replace function public.ingest_row_counts_are_nonnegative(p_row_counts jsonb)
returns boolean
language sql
immutable
strict
set search_path = pg_catalog
as $function$
  select
    jsonb_typeof(p_row_counts) = 'object'
    and not exists (
      select 1
      from jsonb_each(p_row_counts) as entry
      where case
        when jsonb_typeof(entry.value) = 'number' then
          (entry.value #>> '{}')::numeric < 0
          or trunc((entry.value #>> '{}')::numeric) <> (entry.value #>> '{}')::numeric
        else true
      end
    );
$function$;

revoke all on function public.ingest_row_counts_are_nonnegative(jsonb)
  from public, anon, authenticated;
grant execute on function public.ingest_row_counts_are_nonnegative(jsonb)
  to service_role;

