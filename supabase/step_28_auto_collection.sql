      using errcode = 'P0001';
  end if;

  select enrollment.*
  into v_enrollment
  from public.ingest_enrollments as enrollment
  where enrollment.code_hash = p_code_hash
    and enrollment.client_id = v_client_id
  for update;
  if not found then
