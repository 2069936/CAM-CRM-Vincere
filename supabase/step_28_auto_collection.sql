-- Serializing on the device closes the "no row exists yet" race. Once a claim
-- wins, same-payload retries return that row; a reused capture ID or storage path
-- with different immutable payload metadata is rejected.
create or replace function public.claim_ingest_batch(
  p_device_id uuid,
  p_capture_id uuid,
  p_trading_date date,
  p_captured_at timestamptz,
  p_schema_version integer,
  p_storage_path text,
  p_content_sha256 text,
  p_byte_count bigint,
  p_row_counts jsonb
)
returns public.ingest_batches
language plpgsql
security definer
set search_path = pg_catalog, public
as $function$
declare
  v_device public.ingest_devices;
  v_batch public.ingest_batches;
begin
  if p_device_id is null
    or p_capture_id is null
    or p_trading_date is null
    or p_captured_at is null
    or p_schema_version is null
    or p_schema_version <= 0
    or nullif(btrim(p_storage_path), '') is null
    or nullif(btrim(p_content_sha256), '') is null
    or p_byte_count is null
    or p_byte_count < 0
    or p_row_counts is null
    or not public.ingest_row_counts_are_nonnegative(p_row_counts) then
    raise exception 'batch claim metadata is invalid'
      using errcode = '22023';
  end if;

  select device.*
  into v_device
  from public.ingest_devices as device
  where device.id = p_device_id
  for update;

  if not found then
    raise exception 'ingest device is invalid'
      using errcode = 'P0002';
  end if;

  if v_device.status <> 'active' or v_device.revoked_at is not null then
    raise exception 'ingest device is revoked'
      using errcode = '22023';
  end if;

  select batch.*
  into v_batch
  from public.ingest_batches as batch
  where device_id = p_device_id
    and capture_id = p_capture_id
  for update;

  if found then
    if v_batch.trading_date is distinct from p_trading_date
      or v_batch.captured_at is distinct from p_captured_at
      or v_batch.schema_version is distinct from p_schema_version
      or v_batch.storage_path is distinct from p_storage_path
      or v_batch.content_sha256 is distinct from p_content_sha256
      or v_batch.byte_count is distinct from p_byte_count
      or v_batch.row_counts is distinct from p_row_counts then
      raise exception 'capture ID was already claimed with different metadata'
        using errcode = '22023';
    end if;

    return v_batch;
  end if;

  insert into public.ingest_batches (
    capture_id,
    device_id,
    client_id,
    trading_date,
    captured_at,
    status,
    schema_version,
    storage_path,
    content_sha256,
    byte_count,
    row_counts
  )
  values (
    p_capture_id,
    p_device_id,
    v_device.client_id,
    p_trading_date,
    p_captured_at,
    'received',
    p_schema_version,
    p_storage_path,
    p_content_sha256,
    p_byte_count,
    p_row_counts
  )
  returning * into v_batch;

  return v_batch;
end;
$function$;

revoke all on function public.claim_ingest_batch(uuid, uuid, date, timestamptz, integer, text, text, bigint, jsonb)
  from public, anon, authenticated;
grant execute on function public.claim_ingest_batch(uuid, uuid, date, timestamptz, integer, text, text, bigint, jsonb)
  to service_role;

